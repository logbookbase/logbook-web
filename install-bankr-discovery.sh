echo "  ✓ updated src/app/page.tsx (hero: bankr install line)"
cp /tmp/lb-bankr-tmp-$$/docs-page.tsx src/app/docs/page.tsx
echo "  ✓ updated src/app/docs/page.tsx (fixed skill repo URL + source link)"
rm -rf /tmp/lb-bankr-tmp-$$
echo ""
echo "ship it:"
echo "  rm install-bankr-discovery.sh"
echo "  git add . && git commit -m \"feat: surface bankr skill install on landing and docs\" && git push"
exit 0

