#!/usr/bin/env bash
# install-bankr-discovery.sh — surfaces the Bankr skill install command on the site
set -e
if [ ! -f "package.json" ] || ! grep -q "logbook-web" package.json; then
  echo "error: run this from inside the logbook-web project directory"
  exit 1
fi
echo "→ adding Bankr skill discovery to landing + docs..."
ARCHIVE_LINE=$(awk '/^__ARCHIVE_BELOW__/{print NR + 1; exit 0;}' "$0")
mkdir -p /tmp/lb-bankr-tmp-$$
tail -n +$ARCHIVE_LINE "$0" | base64 -d | tar -xzf - -C /tmp/lb-bankr-tmp-$$
